# addition of two numbers already given numbers
num1 = 5
num2 = 10.6
sum = num1 + num2
print("The Sum of", num1, "and", num2, "is: ",sum )

# addition of two numbers take input from user
num1 = float(input("Enter first Number: "))
num2 = float(input("Enter second Number: "))
sum = num1 + num2
print("The Sum of", num1, "and", num2, "is: ",sum )